import numpy as np

a1 = np.zeros(10)
a2 = np.full(10, 2.5)
a3 = np.linspace(1.0, 10.0, 10)
a4 = np.array([2.3, 4.76, 13.1])
print(f'a1 = {a1}')
print(f'a2 = {a2}')
print(f'a3 = {a3}')
print(f'a4 = {a4}')
print(f'a2 + a3 = {a2 + a3}')
print(f'a2 * a3 = {a2 * a3}')
print(f'sqrt(a5) = {np.sqrt(a4)}')
